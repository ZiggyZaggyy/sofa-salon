// Locale: user can switch between English and 中文 (no stacking).

export type Locale = 'en' | 'zh';

const STORAGE_KEY = 'sofa-salon-locale';
const COOKIE_NAME = 'sofa-salon-locale';
export const DEFAULT_LOCALE: Locale = 'en';

/** Preserves supported locale values and otherwise uses the deployment default. */
export function localeFromValue(value: unknown): Locale {
  return value === 'en' || value === 'zh' ? value : DEFAULT_LOCALE;
}

/** Reads current locale from localStorage (client only). */
export function getStoredLocale(fallback: Locale = DEFAULT_LOCALE): Locale {
  if (typeof window === 'undefined') return fallback;
  const stored = localStorage.getItem(STORAGE_KEY);
  return stored === 'en' || stored === 'zh' ? stored : fallback;
}

/** Writes locale to localStorage and cookie so server-rendered pages can read it. */
export function setStoredLocale(locale: Locale): void {
  if (typeof window === 'undefined') return;
  localStorage.setItem(STORAGE_KEY, locale);
  document.cookie = `${COOKIE_NAME}=${locale}; path=/; max-age=31536000; SameSite=Lax`;
}

